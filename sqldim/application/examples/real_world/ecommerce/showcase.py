async def run_ecommerce_showcase():
    """End-to-end e-commerce order intelligence showcase.

    Demonstrates SCD Type 2 customer history, bridge table multi-valued
    campaign attribution, accumulating snapshot order milestones, and
    live schema drift detection via :class:`DimensionalMigrationContext`.
    """
    engine = create_engine(
        "sqlite://", connect_args={"check_same_thread": False}, poolclass=StaticPool
    )
    SQLModel.metadata.create_all(engine)
    print("🛒 E-Commerce Order Intelligence Pipeline\n")
    await _pillar1_scd_customers(engine)
    await _pillar2_bridge_attribution(engine)
    await _pillar3_order_lifecycle(engine)
    await _pillar4_migration_context()
    print(
        "\n🚀 E-Commerce Pipeline complete. SCD2 + Bridge + Accumulating + Migrations."
    )
    engine.dispose()


if __name__ == "__main__":  # pragma: no cover
    asyncio.run(run_ecommerce_showcase())
